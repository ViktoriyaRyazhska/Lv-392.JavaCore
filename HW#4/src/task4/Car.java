package task4;

public class Car {

	private String type;
	private int year;
	private int engCap;

	public Car() {
	}

	public Car(String type, int engCap) {
		this.type = type;
		this.engCap = engCap;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}
	
	
	
}
